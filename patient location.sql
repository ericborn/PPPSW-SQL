FROM patient_encounter pe
JOIN person	p ON pe.person_id = p.person_id
WHERE (pe.create_timestamp >= '20140101' AND pe.create_timestamp <= '20170518') 
AND (pe.billable_ind = 'Y' AND pe.clinical_ind = 'Y')
AND p.last_name NOT IN ('Test', 'zztest', 'zzpatient', '4.0Test', 'Test OAS', '3.1test', 'chambers test', 'zztestadult', 'Anonymous', 'Patient')
AND pe.location_id NOT IN ('518024FD-A407-4409-9986-E6B3993F9D37', '3A067539-F112-4304-931D-613F7C4F26FD')
--AND 'count' > '100'
group by city
order by 'count' desc

